    <h1 class="title">INDICATORS OF HUMAN TRAFFICKING</h1>

    <p>
      People being trafficked will not always or even often identify as trafficking
      victims. Because of how trafficking works, most people do not identify their
      experience as trafficking until AFTER the situation is over. Therefore, it is
      important to know the signs.
    </p>

    <p>
      Indicators of Human Trafficking can include, but are not limited, to one or more of
      the following:
    </p>

    <ul class="list">
      <li>The person appears disconnected from family, friends, or community.</li>
      <li>The child or teen is frequently absent or has stopped attending school.</li>
      <li>The person has a sudden or dramatic change in behavior.</li>
      <li>
        The person appears disoriented or confused or shows signs of mental or physical
        abuse.
      </li>
      <li>The person has bruises in various stages of healing.</li>
      <li>The person seems fearful, timid, or submissive.</li>
      <li>
        The person shows signs of having been denied food, water, sleep, or medical care.
      </li>
      <li>The person is often in the company of someone to whom he or she defers.</li>
      <li>The person appears to be coached on what to say.</li>
      <li>
        The person lives in unsuitable or unstable conditions or lacks personal
        possessions.
      </li>
      <li>
        The person does not appear to be able to freely leave where they live or work.
      </li>
    </ul>

