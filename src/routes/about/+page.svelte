    <h2>Our Purpose</h2>
    <p>
      Our Purpose is to create universal awareness of human trafficking and kidnapping
      distress hand signal so that any person who is or is about to become a victim of
      human trafficking, or be held by any force against ones will, can obtain help and
      their trafficker or abductor, apprehended.
    </p>

    <h2>The Signal</h2>

    <p>
      The Signal is a 2022 Telly Award winning PSA film with over 2M views. It's a Rotary
      & Retired military outreach project bringing a versatile highly effective distress
      SOS Signal to youth victims around the globe. Share The Signal is a non-profit,
      registered 501(c)(3).
    </p>

    <h2>Rancho Santa Fe Rotary</h2>
    <p>
      Rotary’s motto "Service Before Self" is very well exemplified in this chapter who is
      well known for their many community outreach programs including Human Trafficking.
    </p>

    <p>
      Don Meredith, Rotary past president, is the founder of Share the Signal and is
      focused on getting The Signal out as far and wide as possible. He is collaborating
      and speaking with other philanthropic organizations for this cause and is building a
      network of volunteers to help promulgate the hand signal.
    </p>

