    <h2>Our Military Partner</h2>

    <p>
      Lieutenant Commander Doug Lito – fighter pilot US Navy (ret) – Special instructions
      officer at the combined air operations during operation enduring freedom.
    </p>

    <p>
      He devised all the secret code words and signals used by international allied forces
      to authenticate and recover personnel trapped behind enemy lines.
    </p>

    <p>
      He has said, "no other hand signal would conflict with <em>The Signal</em>" and that
