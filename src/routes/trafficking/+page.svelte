    <h2>VICTIMS</h2>
    <p>Victims of Human Trafficking can be anyone: women and men, adults and children, citizens, and noncitizens alike. Victims can be found in legal and illegal industries including child and elder care, massage parlors, hair salons, restaurants, dance clubs, hotels, and factories. Other victims are hidden in domestic servitude in private homes.</p>
    
    <h2>TRAFFICKERS</h2>
    <p>Traffickers come from all genders, races, ethnicities, and income levels. In the great majority of situations, victims know and even trust or love their traffickers. The trafficker may be an intimate partner or spouse of the victim, a family member, friend, or co-worker. Traffickers target vulnerable people, offering emotional or financial support. Victims are deceived by false promises of love, a good job, or a stable life and are lured or forced into deplorable living or work situations.</p>
    
    <h2>RECRUITERS</h2>
    <p>A growing source of recruiting for Human Traffickers is the Internet. Traffickers use social media accounts such as Facebook and Instagram, dating websites, and gaming apps to connect with and recruit young and vulnerable victims. Victims are frequently lured to secondary chatrooms or private forums. Traffickers often use sophisticated anonymity software and create fake social media, marketing themselves as employment agencies.</p>
    
