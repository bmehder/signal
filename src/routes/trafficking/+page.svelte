    <h1 class="title">What is Human Trafficking?</h1>

    <p>Human Trafficking is a form of modern-day slavery. It a Federal crime defined as the use of <strong>force, fraud, or coercion</strong> to compel a person into commercial sex acts or forced labor against their will.</p>
    
    <p>The <strong>Action-Means-Purpose Model (AMP)</strong> outlines the core elements of trafficking as occurring when a perpetrator, often referred to as a trafficker, takes an <strong>Action</strong> (induces, recruits, harbors, transports, provides), and then employs the <strong>Means</strong> of force, fraud, or coercion for the <strong>Purpose</strong> of compelling the victim to provide commercial sex acts or labor or services.</p> 
    
    <p>At a minimum, one element from each column must be present. Where a person younger than 18 is induced to perform a commercial sex act, it is a crime regardless of whether there is any force, fraud, or coercion.</p>
